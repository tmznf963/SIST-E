
public class Pmp {
   int num;
   String code;
   int days;
   int age;
   String part;
   int fee;
   int cost;
   int price;
   public Pmp(int num, String code, int days, int age) {
      super();
      this.num = num;
      this.code = code;
      this.days = days;
      this.age = age;
   }
   public int getNum() {
      return num;
   }
   public void setNum(int num) {
      this.num = num;
   }
   public String getCode() {
      return code;
   }
   public void setCode(String code) {
      this.code = code;
   }
   public int getDays() {
      return days;
   }
   public void setDays(int days) {
      this.days = days;
   }
   public int getAge() {
      return age;
   }
   public void setAge(int age) {
      this.age = age;
   }
   public String getPart() {
      return part;
   }
   public void setPart(String part) {
      this.part = part;
   }
   public int getFee() {
      return fee;
   }
   public void setFee(int fee) {
      this.fee = fee;
   }
   public int getCost() {
      return cost;
   }
   public void setCost(int cost) {
      this.cost = cost;
   }
   public int getPrice() {
      return price;
   }
   public void setPrice(int price) {
      this.price = price;
   }
}