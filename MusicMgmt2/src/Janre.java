class Janre {
	private String name;  
	private int kyonggi;      
	private int youngnam;   
	private int choongchung;   
	private int num;                
	
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	int getKyongi() {
		return kyonggi;
	}
	void setKyonggi(int kyonggi) {
		this.kyonggi = kyonggi;
	}
	int getYoungnam() {
		return youngnam;
	}
	void setYoungnam(int youngnam) {
		this.youngnam = youngnam;
	}
	int getChoongchung() {
		return choongchung;
	}
	void setChoongchung(int choongchung) {
		this.choongchung = choongchung;
	}
	int getNum() {
		return num;
	}
	void setNum(int num) {
		this.num = num;
	}
}
